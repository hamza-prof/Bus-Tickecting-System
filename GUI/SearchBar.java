    private void show_hideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show_hideActionPerformed
        if (busName.getText() != null) {
            if (show_hide.getText().equalsIgnoreCase("show seats")) {
                show_hide.setText("Hide Seats");
                seatp1.setVisible(true);
                seatp2.setVisible(true);
                try {
                    setSeats();
                } catch (SQLException ex) {
                    Logger.getLogger(SearchBar.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (show_hide.getText().equalsIgnoreCase("hide seats")) {
                show_hide.setText("Show Seats");
                seatp1.setVisible(false);
                seatp2.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No bus exist at required time!");
        }
    }//GEN-LAST:event_show_hideActionPerformed


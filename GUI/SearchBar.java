    private void searchBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBusActionPerformed
        pickup = pickupt.getText();
        drop = dropt.getText();
        date = ((JTextField) datet.getDateEditor().getUiComponent()).getText().toString();
        time = timet.getText();
        category = categoryt.getText();
        srch = new SearchingBus(pickup, drop, time, category);
        
        try {
            bus_data = srch.getBusData();
        } catch (SQLException ex) {
            Logger.getLogger(SearchBar.class.getName()).log(Level.SEVERE, null, ex);
        }
        //int i = 0;
//        for (String val : bus_data) {
//            System.out.println(val);
//            //i++;
//        }
        //System.out.println("Great");
        if (bus_data[0] != null) {
            busName.setText("Bus Number: "+bus_data[0]);
            lcategory.setText("Category: "+category);
            dtime.setText(bus_data[2]);
            atime.setText(bus_data[3]);
            from.setText("From: "+pickup);
            to.setText("To: "+drop);
            price.setText("Price: "+bus_data[4]);
            aseats.setText("Available Seats: " + bus_data[1]);
            hours.setText(srch.hours(dtime.getText(), atime.getText()) + " Hours");
            show_hide.setEnabled(true);
            booked.setEnabled(true);
        } else {
            show_hide.setEnabled(false);
            booked.setEnabled(false);
            JOptionPane.showMessageDialog(null, "No bus exist at required time!");
        }

    }//GEN-LAST:event_searchBusActionPerformed


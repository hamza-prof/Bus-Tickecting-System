    private void setSeatColor(String seat_id, Map.Entry<String, String[]> entry) {
        JPanel[] panel = {s1, s2, s3, s4, s5, s6, s7, s8, s9, s10, s11, s12, s13, s14, s15, s16, s17,
            s18, s19, s20, s21, s22, s23, s24, s25, s26, s27, s28, s29, s30, s31, s32,
            s33, s34, s35, s36, s37, s38, s39};
        String[] str = entry.getValue();
        for (int i = 0; i < 39; i++) {
            if (i < 9) {
                if (seat_id.equalsIgnoreCase("s0" + (i + 1))) {
                    setColor(panel[i], str);
                }
            } else {
                if (seat_id.equalsIgnoreCase("s" + (i + 1))) {
                    setColor(panel[i], str);
                }
            }
        }
    }

    private void setColor(JPanel s, String[] str) {
        if (str == null) {
            s.setBackground(Color.WHITE);
        } else if (str[2].equalsIgnoreCase("female")) {
            s.setBackground(Color.PINK);
        } else {
            s.setBackground(Color.GRAY);
        }
    }
